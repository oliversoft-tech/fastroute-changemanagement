-- 6) View para materializar documento canonico por linha relacional
-- O extractor deve ler daqui para montar kb_documents.
create or replace view public.kb_document_candidates as
select
  'impact_analysis'::text as source_table,
  ia.id::text as source_pk,
  ia.id::bigint as analysis_id,
  null::text as repo_name,
  'analysis'::text as kind,
  ('Impact Analysis #' || ia.id::text) as title,
  coalesce(ia.change_summary, '') as content,
  md5(coalesce(ia.change_summary, '')) as content_hash,
  jsonb_build_object(
    'complexity_level', ia.complexity_level,
    'created_at', ia.created_at,
    'updated_at', ia.updated_at
  ) as metadata
from public.impact_analysis ia

union all

select
  'changes_in_repo'::text as source_table,
  c.id::text as source_pk,
  ia.id::bigint as analysis_id,
  r.name as repo_name,
  'change'::text as kind,
  ('Change #' || c.id::text || ' - ' || coalesce(r.name, 'repo')) as title,
  coalesce(c.change_summary, '') as content,
  md5(coalesce(c.change_summary, '')) as content_hash,
  jsonb_build_object(
    'repo_id', c.repo_id,
    'created_at', c.created_at,
    'updated_at', c.updated_at
  ) as metadata
from public.changes_in_repo c
left join public.repos r on r.id = c.repo_id
left join lateral (
  select i.id
  from public.impact_analysis i
  where c.change_summary like ('[impact:' || i.id::text || ']%')
  order by i.id desc
  limit 1
) ia on true

union all

select
  'code_changes'::text as source_table,
  cc.id::text as source_pk,
  ia.id::bigint as analysis_id,
  r.name as repo_name,
  'code'::text as kind,
  ('Code Change #' || cc.id::text || ' - ' || coalesce(r.name, 'repo')) as title,
  coalesce(cc.code, '') as content,
  md5(coalesce(cc.code, '')) as content_hash,
  jsonb_build_object(
    'change_id', cc.change_id,
    'created_at', cc.created_at,
    'updated_at', cc.updated_at
  ) as metadata
from public.code_changes cc
left join public.changes_in_repo c on c.id = cc.change_id
left join public.repos r on r.id = c.repo_id
left join lateral (
  select i.id
  from public.impact_analysis i
  where c.change_summary like ('[impact:' || i.id::text || ']%')
  order by i.id desc
  limit 1
) ia on true

union all

select
  'testsuites_for_change'::text as source_table,
  ts.id::text as source_pk,
  ia.id::bigint as analysis_id,
  r.name as repo_name,
  'testsuite'::text as kind,
  ('Testsuite #' || ts.id::text || ' - ' || coalesce(r.name, 'repo')) as title,
  coalesce(ts.description, '') as content,
  md5(coalesce(ts.description, '')) as content_hash,
  jsonb_build_object(
    'change_id', ts.change_id,
    'created_at', ts.created_at,
    'updated_at', ts.updated_at
  ) as metadata
from public.testsuites_for_change ts
left join public.changes_in_repo c on c.id = ts.change_id
left join public.repos r on r.id = c.repo_id
left join lateral (
  select i.id
  from public.impact_analysis i
  where c.change_summary like ('[impact:' || i.id::text || ']%')
  order by i.id desc
  limit 1
) ia on true

union all

select
  'tests_for_testsuite'::text as source_table,
  t.id::text as source_pk,
  ia.id::bigint as analysis_id,
  r.name as repo_name,
  'test'::text as kind,
  ('Test #' || t.id::text || ' - ' || coalesce(r.name, 'repo')) as title,
  coalesce(t.description, '') as content,
  md5(coalesce(t.description, '')) as content_hash,
  jsonb_build_object(
    'testsuite_id', t.testsuite_id,
    'created_at', t.created_at,
    'updated_at', t.updated_at
  ) as metadata
from public.tests_for_testsuite t
left join public.testsuites_for_change ts on ts.id = t.testsuite_id
left join public.changes_in_repo c on c.id = ts.change_id
left join public.repos r on r.id = c.repo_id
left join lateral (
  select i.id
  from public.impact_analysis i
  where c.change_summary like ('[impact:' || i.id::text || ']%')
  order by i.id desc
  limit 1
) ia on true;

